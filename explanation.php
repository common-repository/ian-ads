  <h1>What is this for?</h1>
  <p>If you have signed up for IAN, you can use this plugin to easily insert ads with no copying and pasting required.</p>
  <hr>
  <h2>What is IAN?</h2>
  <p>IAN stands for Interactive Ad Network. We help you earn revenue by displaying tasteful ads on your site. You can learn more about what we do <a href="http://interactivead.network" target="_blank">here</a>.</p>
  <hr>
  <h2>What if I haven't signed up?</h2>
  <p>That's OK, you can register on our site for free: <a href="http://interactivead.network/signup" target="_blank">Sign up here</a></p>
  <p>There are basically three steps to getting ads displaying on your Wordpress site:</p>
  <ol>
    <li><a href="http://interactivead.network/signup" target="_blank">Signup</a> for an account and request an Ad Slot (this is what we call the space we display ads on your site).</li>
    <li>Once the ad slot is approved, install and activate this plugin (good job, already done with that!)</li>
    <li>Either add an IAN Ad Slot Widget under Appearances -> Widgets (if your theme supports widgets) or use our shortcode to insert the ad slot into your page.</li>
  </ol>
  <p><b>Note:</b> It is important when you signup that you put the domain name of your site in correctly for this plugin to work.</p>
  <hr>
  <h2>How do I add an Ad Slot to my site?</h2>
  <h3>If your site supports widgets:</h3>
  <ol>
    <li>Go to <a href="<?php echo site_url('/wp-admin/widgets.php'); ?>">Widgets</a>.</li>
    <li>Look for the Widget on the left side titled "IAN Ad".</li>
    <li>Click and drag it over to the widget area on the right that you want to add it to.</li>
    <li>If you have more than one Ad Slot you'll need to choose the one you want to add, otherwise it will just show you what Ad Slot it will use.</li>
    <li>Click on the "Save" button for the widget.</li>
  </ol>
  <p>Note that if you know how to customize your theme you can also add in a widget that way as well.</p>
  <h3>If you want to add an Ad Slot to a page or post:</h3>
  <p>Some Wordpress themes don't support widgets. If that is the case you'll want to use our shortcode. You can think of a shortcode as a sort of
    marker in your content that says, "when you display this page/post, I want you to insert something extra at this spot". Examples of shortcodes
    are [video], [gallery] or [audio]. In each of these they will insert a video, gallery of pictures or an audio player for an audio clip. No need
    to understand programming or HTML to get them to work. You'll notice that a shortcode is identified by the square brackets, [], around it. Our
    shortcode is [ian-ad]. To add the shortcode to your page/post:</p>
  <ol>
    <li>Edit the page or post that you want to add the Ad Slot to.</li>
    <li>In the editor that shows the content of your page or post, locate where you want the Ad Slot to appear.</li>
    <li>Type in [ian-ad] in the place that you want the ad to appear.</li>
    <li>Update, Save as Draft or Publish it.</li>
  </ol>
  <p>It is strongly recommended to put the shortcode on its own line in your content, like so:</p>
  <div class="ian-example-content">
    <p>...and this is the end of one paragraph here.</p>
    <p>[ian-ad]</p>
    <p>Beginning a new paragraph after the Ad slot looks best...</p>
  </div>
  <p>Doing it this way will allow the Ad to display without messing up the formatting of your content.</p>
  <h4>If you have more than one Ad Slot</h4>
  <p>If you have more than one Ad Slot you can indicate which one you want to use by pulling the code of the Ad Slot and adding it to the shortcode.</p>
  <p>As an example, let's say that my Ad Slot code was 23245. I would make the shortcode look like the following:</p>
  <p><b>[ian-ad code="23245"]</b></p>
  <p>That will display that Ad Slot at that location. If you want to use the first Ad Slot (as shown above) don't need to add the <i>code="23245"</i>
    to the shortcode. It will automatically use the first one by default.</p>
